//Template Literals (Template Strings)

//Template Literals and enclosed by the backticks (``)

//There are used to 
// Embed variables or expressions in the strings 
//write multiline strings 

// `some text ${expressions} some text`

let first = 'Naresh';
console.log(`Hello ${first}!`);

let sum = `The sum of 4 and 5 is ${4+5}`;
console.log(sum);

let person = {name:"Naresh"}
console.log(`My name is ${person.name}!`)

// multiline strings 
let nam = "Ram";
console.log(`Hello ${nam},
Good morning,
How are you ?
`)